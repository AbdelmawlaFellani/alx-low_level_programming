#include "3-calc.h"

/**
 * op_add - returns the sum of 2 numbers
 *
 * @a: 1st Number Input
 * @b: 2nd Number Input
 *
 * Return: int (Sum of the @a and @b)
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the substraction of 2 numbers
 *
 * @a: 1st Number Input
 * @b: 2nd Number Input
 *
 * Return: int (Substraction of the @a and @b)
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns the product of 2 numbers
 *
 * @a: 1st Number Input
 * @b: 2nd Number Input
 *
 * Return: int (Product of the @a and @b)
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the division result of 2 numbers
 *
 * @a: 1st Numbers Input
 * @b: 2nd Numbers Input
 *
 * Return: int (Division Result of @a by @b)
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the reminder of the division of 2 numbers
 *
 * @a: 1st Number Input
 * @b: 2nd Number Input
 *
 * Return: int (Division Reminder of @a by @b)
 */
int op_mod(int a, int b)
{
	return (a % b);
}
